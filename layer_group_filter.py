    def _cached_entries_to_pairs(self, cached):
        """캐시 항목: 구버전 list[str] 또는 [(code, display), ...]."""
        if not cached:
            return []
        first = cached[0]
        if isinstance(first, (list, tuple)) and len(first) >= 2:
            return [(str(a), str(b)) for a, b in cached]
        return [(str(x), str(x)) for x in cached]

