    def _collect_code_to_label_map(self, layer, code_field, label_field):
        """코드값별 첫 번째 설명(라벨) 매핑. getFeatures가 비어도 uniqueValues와 합치면 목록은 유지."""
        code_idx = layer.fields().indexOf(code_field)
        label_idx = layer.fields().indexOf(label_field)
        if code_idx < 0 or label_idx < 0:
            return {}
        pairs = {}
        current_filter = layer.subsetString()
        try:
            layer.setSubsetString("")

            def _scan(use_subset):
                req = QgsFeatureRequest().setFlags(QgsFeatureRequest_NoGeometry)
                if use_subset:
                    # QGIS 3.40+ PyQt6: 필드명(str) 목록이어야 함(int면 "index 0 has type int but str is expected")
                    req.setSubsetOfAttributes([code_field, label_field], layer.fields())
                for f in layer.getFeatures(req):
                    cv = f.attribute(code_idx)
                    if cv is None:
                        continue
                    k = str(cv)
                    if k in pairs:
                        continue
                    lv = f.attribute(label_idx)
                    pairs[k] = str(lv) if lv is not None else k

            _scan(True)
            if not pairs and layer.featureCount() > 0:
                _scan(False)
        finally:
            try:
                layer.setSubsetString(current_filter)
            except Exception:
                pass
        return pairs

    def _unique_code_strings(self, layer, dp, field_index):
        """subset 해제 후 uniqueValues로 코드 문자열 목록(정렬)."""
        current_filter = layer.subsetString()
        try:
            layer.setSubsetString("")
            values = dp.uniqueValues(field_index)
        finally:
            try:
                layer.setSubsetString(current_filter)
            except Exception:
                pass
        str_values = [str(v) for v in values if v is not None]
        return sorted(str_values, key=lambda x: (x.lower(), x))

    def _build_filter_value_entries(self, layer, dp, code_field, field_index, label_field_name):
        """(code, display) 목록. 설명 컬럼이 있어도 코드 목록은 항상 uniqueValues로 확보."""
        codes = self._unique_code_strings(layer, dp, field_index)
        if not label_field_name:
            return [(s, s) for s in codes]

        label_map = self._collect_code_to_label_map(layer, code_field, label_field_name)
        if label_map and len(label_map) < len(codes):
            QgsMessageLog.logMessage(
                f"표시용 컬럼 매핑이 일부 코드에만 있습니다(피처 스캔 {len(label_map)}/{len(codes)}).",
                tag="Smart Layer Filter",
                level=Qgis_Info,
            )
        entries = []
        for code in codes:
            lab = label_map.get(code)
            entries.append((code, self._format_filter_value_display(lab if lab is not None else code, code)))
        return entries

