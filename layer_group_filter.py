def _provider_can_scan_unique_values(provider):
    """고유값 수집은 속성 편집(ChangeAttributeValues)과 무관. ZIP/OGR 읽기 전용도 조회 시도."""
    return provider is not None


def _vector_layers_in_group(group):
    """그룹 내 벡터 레이어 목록(트리 순서)."""
    out = []
    if not group:
        return out
    for tree_layer in group.findLayers():
        lyr = tree_layer.layer()
        if isinstance(lyr, QgsVectorLayer) and lyr.dataProvider():
            out.append(lyr)
    return out


def _field_names_for_filter_ui(layer):
    """Filter Attribute / Value label 콤보에 넣을 필드명(시스템 필드 제외)."""
    exclude_fields = {"gid", "fid", "objectid", "id", "oid", "pk", "primary_key"}
    return [
        f.name()
        for f in layer.fields()
        if f.name().lower() not in exclude_fields
    ]


def _field_names_for_locate(layer):
    """속성 찾기 탭: 모든 속성 필드명."""
    return [f.name() for f in layer.fields()]


def _expression_quote_field(name):
    """QgsExpression: 필드명 인용(버전별 API 차이 대응)."""
    qe = QgsExpression
    if hasattr(qe, "quoteFieldIdentifier"):
        return qe.quoteFieldIdentifier(name)
    if hasattr(qe, "quotedColumnRef"):
        return qe.quotedColumnRef(name)
    return '"' + str(name).replace('"', '""') + '"'


def _expression_quote_string_literal(text):
    """QgsExpression: 문자열 리터럴 인용(검색어)."""
    qe = QgsExpression
    if hasattr(qe, "quotedValue"):
        return qe.quotedValue(text)
    if hasattr(qe, "quotedString"):
        return qe.quotedString(str(text))
    return "'" + str(text).replace("'", "''") + "'"


class FilterValueTask(QgsTask):
    """백그라운드에서 필터 값을 가져오는 작업"""
    valuesReady = pyqtSignal(list, str)  # values, layer_id
    
    def __init__(self, layer, fieldIndex, layer_id):
        super().__init__()
        self.layer = layer
        self.fieldIndex = fieldIndex
        self.layer_id = layer_id
        self._is_cancelled = False
        self._is_completed = False
        QgsMessageLog.logMessage(f"FilterValueTask initialized: layer={layer.name()}, fieldIndex={fieldIndex}, layer_id={layer_id}", level=Qgis_Info)
        
    def cancel(self):
        """작업 취소 시 호출되는 메서드"""
        if not self._is_cancelled:
            self._is_cancelled = True
            super().cancel()
            QgsMessageLog.logMessage("Task cancelled", level=Qgis_Info)
        
    def finished(self, result):
        """작업 완료 시 호출되는 메서드"""
        if not self._is_completed:
            self._is_completed = True
            super().finished(result)
            QgsMessageLog.logMessage("Task completed", level=Qgis_Info)
        
    def run(self):
        try:
            if self._is_cancelled:
                return False
                
            QgsMessageLog.logMessage(f"Starting FilterValueTask.run for layer: {self.layer.name()}", level=Qgis_Info)
            # uniqueValues를 사용하여 고유값을 가져옴
            provider = self.layer.dataProvider()
            if _provider_can_scan_unique_values(provider):
                field_name = self.layer.fields().at(self.fieldIndex).name()
                QgsMessageLog.logMessage(f"Getting unique values for field: {field_name}", level=Qgis_Info)
                
                # 현재 필터 문자열 저장
                current_filter = self.layer.subsetString()
                
                # 필터 제거
                self.layer.setSubsetString("")
                
                # uniqueValues 메서드를 사용하여 고유값 가져오기
                values = provider.uniqueValues(self.fieldIndex)
                
                # 원래 필터 복원
                self.layer.setSubsetString(current_filter)
                
                if self._is_cancelled:
                    return False
                    
                if values:
                    # None 값 제외하고 문자열로 변환
                    values = [str(v) for v in values if v is not None]
                    QgsMessageLog.logMessage(f"Found {len(values)} unique values", level=Qgis_Info)
                    self.valuesReady.emit(values, self.layer_id)
                    return True
                else:
                    QgsMessageLog.logMessage("No unique values found", level=Qgis_Warning)
            else:
                QgsMessageLog.logMessage("Provider not available or cannot scan unique values", level=Qgis_Warning)
            return False
        except Exception as e:
            QgsMessageLog.logMessage(f"Error in FilterValueTask: {str(e)}", level=Qgis_Critical)
            return False

