    def createSubsetString(self, layer, fieldIndex, values, presetIndex):
        """필터 문자열 생성"""
        if presetIndex is None:
            presetIndex = -1
            
        if not values: #set default value from GUI as list with single value
            values = []
            current_value = self._current_filter_value_code()
            if current_value and current_value != "Loading...":
                values.append(current_value)
            else:
                return ""  # 유효한 값이 없는 경우 빈 문자열 반환
            
        # presetIndex 유효성 검사
        if presetIndex < 0 or presetIndex >= len(self.filterPresets):
            return ""  # 유효하지 않은 인덱스인 경우 빈 문자열 반환
        
        #look up presetXml (XML object) 
        presetXml = self.filterPresets[presetIndex][1]
        
        #save filter operator
        filterOper = presetXml.find('operator').text
        
        #save filterColums as list of attibure name strings
        filterColumns = [col.text for col in presetXml.findall('column')]     
            
        #check if preset has ValueRelation, if so replace the value with a list of corresponing key's   
        vr = self.GetValueRelation(presetXml)
        if vr is not None:  # 명시적으로 None 체크
            values = self.getValueRelation(values[0], vr, invert=True, getAll=True)
            if not values:  # ValueRelation 결과가 None인 경우
                return ""  # 빈 문자열 반환
            
        # 필터 문자열 생성
        subsetString = ""
        for filterColumn in filterColumns:
            if filterColumn in layer.fields().names():
                firstValue = True
                for value in values:
                    if value is not None:  # None 값 제외
                        #if muliple values create 'OR' relations
                        if firstValue:                       
                            addSubSetstring = "\"{0}\" {1} '{2}'".format(filterColumn, filterOper, value)
                            firstValue = False
                        else:
                            addSubSetstring = addSubSetstring + " or " + "\"{0}\" {1} '{2}'".format(filterColumn, filterOper, value)
                
                #append the new SubSetstring with 'AND'relation to the current SubSetstring
                if subsetString == "":
                    subsetString = addSubSetstring
                else:
                    subsetString = subsetString + " and " + addSubSetstring
                    
        return subsetString

    def replaceFilters(self):
        """기존 필터를 새로운 필터로 교체"""
        try:
            current_field = self.dockwidget.filterAttributeComboBox.currentText()
            current_operator = self._current_filter_operator_symbol()
            current_value = self._current_filter_value_code()
            
            if current_field and current_value and current_value != "Loading...":
                # 필터 문자열 생성
                if current_operator in ['LIKE', 'ILIKE']:
                    filter_text = f'"{current_field}" {current_operator} \'%{current_value}%\''
                else:
                    filter_text = f'"{current_field}" {current_operator} \'{current_value}\''
                    
                QgsMessageLog.logMessage(f"Replacing filters with: {filter_text}", level=Qgis_Info)

                layer_ids = []
                for vl, lid in self._iter_filter_target_vector_layers():
                    vl.setSubsetString(filter_text)
                    layer_ids.append(lid)
                if layer_ids:
                    self.iface.mapCanvas().refresh()
                    for lid in layer_ids:
                        self.iface.layerTreeView().refreshLayerSymbology(lid)
        except Exception as e:
            QgsMessageLog.logMessage(f"Error in replaceFilters: {str(e)}", level=Qgis_Critical)

    def addFilter(self):
        """기존 필터에 새로운 필터 추가"""
        try:
            current_field = self.dockwidget.filterAttributeComboBox.currentText()
            current_operator = self._current_filter_operator_symbol()
            current_value = self._current_filter_value_code()
            
            if current_field and current_value and current_value != "Loading...":
                # 새로운 필터 문자열 생성
                if current_operator in ['LIKE', 'ILIKE']:
                    new_filter = f'"{current_field}" {current_operator} \'%{current_value}%\''
                else:
                    new_filter = f'"{current_field}" {current_operator} \'{current_value}\''
                    
                QgsMessageLog.logMessage(f"Adding filter: {new_filter}", level=Qgis_Info)

                layer_ids = []
                for vl, lid in self._iter_filter_target_vector_layers():
                    current_filter = vl.subsetString()
                    if current_filter:
                        vl.setSubsetString(f"({current_filter}) AND ({new_filter})")
                    else:
                        vl.setSubsetString(new_filter)
                    layer_ids.append(lid)
                if layer_ids:
                    self.iface.mapCanvas().refresh()
                    for lid in layer_ids:
                        self.iface.layerTreeView().refreshLayerSymbology(lid)
        except Exception as e:
            QgsMessageLog.logMessage(f"Error in addFilter: {str(e)}", level=Qgis_Critical)

