    def _locate_run_search(self):
        layer = self.iface.activeLayer()
        d = self.dockwidget
        if not isinstance(layer, QgsVectorLayer) or not layer.dataProvider():
            QMessageBox.information(
                d,
                self.tr("속성 찾기"),
                self.tr("레이어 패널에서 벡터 레이어를 선택하세요."),
            )
            return
        field_name = d.locateFieldComboBox.currentText()
        if not field_name:
            QMessageBox.information(
                d,
                self.tr("속성 찾기"),
                self.tr("검색할 속성을 선택하세요."),
            )
            return
        search_text = d.locateSearchLineEdit.text().strip()
        if not search_text:
            QMessageBox.information(
                d,
                self.tr("속성 찾기"),
                self.tr("검색어를 입력하세요."),
            )
            return
        exp = self._locate_expression(field_name, search_text)
        if exp.hasParserError():
            QMessageBox.warning(
                d,
                self.tr("속성 찾기"),
                self.tr("식 오류: {0}").format(exp.parserErrorString()),
            )
            return
        req = QgsFeatureRequest(exp).setLimit(self.LOCATE_MAX_RESULTS + 1)
        fidx = layer.fields().lookupField(field_name)
        if fidx < 0:
            QMessageBox.warning(d, self.tr("속성 찾기"), self.tr("필드를 찾을 수 없습니다."))
            return
        lw = d.locateResultsListWidget
        lw.clear()
        d.locateStatusLabel.setText(self.tr("검색 중…"))
        QApplication.processEvents()
        QApplication.setOverrideCursor(Qt_WaitCursor)
        feats = []
        try:
            feats = list(layer.getFeatures(req))
        finally:
            QApplication.restoreOverrideCursor()
            QApplication.processEvents()
        truncated = len(feats) > self.LOCATE_MAX_RESULTS
        feats = feats[: self.LOCATE_MAX_RESULTS]
        lid = layer.id()
        for feat in feats:
            row = self._locate_format_result_row(layer, feat, fidx)
            it = QListWidgetItem(row)
            it.setData(Qt_UserRole, feat.id())
            it.setData(Qt_UserRole1, lid)
            tip = self._locate_result_tooltip(layer, feat, fidx)
            if tip:
                it.setToolTip(tip)
            lw.addItem(it)
        if not feats:
            d.locateStatusLabel.setText(self.tr("일치 없음"))
            QMessageBox.information(
                d,
                self.tr("속성 찾기"),
                self.tr("일치하는 피처가 없습니다."),
            )
        else:
            msg = self.tr("{0}건 — 목록에서 줄을 클릭해 선택한 뒤 이동·확대하세요.").format(
                len(feats)
            )
            d.locateStatusLabel.setText(msg)
            if truncated:
                d.locateStatusLabel.setText(
                    msg
                    + " "
                    + self.tr("(상한 {0}건까지만 표시)").format(self.LOCATE_MAX_RESULTS)
                )
                QgsMessageLog.logMessage(
                    self.tr("일치 피처가 많아 최대 {0}건만 목록에 표시했습니다.").format(
                        self.LOCATE_MAX_RESULTS
                    ),
                    tag="Smart Layer Filter",
                    level=Qgis_Info,
                )
            if lw.count() > 0:
                lw.setCurrentRow(0)
        self._locate_results_layer_id = layer.id()

    def _locate_resolve_selection(self):
        item = self.dockwidget.locateResultsListWidget.currentItem()
        if item is None:
            return None, None, None
        fid = item.data(Qt_UserRole)
        lid = item.data(Qt_UserRole1)
        if lid is None or fid is None:
            return None, None, None
        lyr = QgsProject.instance().mapLayer(lid)
        if not isinstance(lyr, QgsVectorLayer):
            return None, None, None
        feat = lyr.getFeature(fid)
        if not feat.isValid():
            return None, None, None
        return lyr, feat, item

    def _locate_geometry_for_canvas(self, layer, geom):
        """레이어에 저장된 CRS의 지오메트리를 지도(캔버스 destinationCrs)로 변환."""
        if geom is None or geom.isNull():
            return None
        canvas = self.iface.mapCanvas()
        dest = canvas.mapSettings().destinationCrs()
        src = layer.crs()
        g = QgsGeometry(geom)
        if not src.isValid() or not dest.isValid():
            return g
        if src.authid() == dest.authid() or src == dest:
            return g
        xform = QgsCoordinateTransform(src, dest, QgsProject.instance())
        try:
            xform.setBallparkTransformsAreAppropriate(True)
        except Exception:
            pass
        try:
            g.transform(xform)
        except Exception as e:
            QgsMessageLog.logMessage(
                self.tr("좌표 변환 실패: {0}").format(str(e)),
                tag="Smart Layer Filter",
                level=Qgis_Warning,
            )
            return None
        if g.isNull():
            return None
        return g

    def _locate_focal_point_xy(self, geom):
        """이동·중심 계산용: 점은 좌표, 면·선은 centroid, 실패 시 bbox 중심."""
        if geom is None or geom.isNull():
            return None
        try:
            if geom.type() == QgsWkb_PointGeometry:
                if geom.isMultipart():
                    mp = geom.asMultiPoint()
                    if mp:
                        p = mp[0]
                        return QgsPointXY(p)
                return QgsPointXY(geom.asPoint())
            cen = geom.centroid()
            if cen is not None and not cen.isNull():
                return QgsPointXY(cen.asPoint())
        except Exception:
            pass
        return geom.boundingBox().center()

    def _locate_extent_for_zoom(self, canvas, geom):
        """확대용 범위: 여유 마진을 두어 화면에서 잘 보이게."""
        if geom is None or geom.isNull():
            return None
        try:
            mupp = canvas.mapSettings().mapUnitsPerPixel()
        except Exception:
            mupp = 1.0
        rect = geom.boundingBox()
        if rect.width() < 1e-12 and rect.height() < 1e-12:
            c = rect.center()
            d = max(mupp * 45.0, 1.0)
            return QgsRectangle(c.x() - d, c.y() - d, c.x() + d, c.y() + d)
        cx = rect.center().x()
        cy = rect.center().y()
        half_w = rect.width() / 2.0
        half_h = rect.height() / 2.0
        margin_x = max(half_w * 0.45, mupp * 18.0)
        margin_y = max(half_h * 0.45, mupp * 18.0)
        return QgsRectangle(
            cx - half_w - margin_x,
            cy - half_h - margin_y,
            cx + half_w + margin_x,
            cy + half_h + margin_y,
        )

    def _locate_map_feedback(self, layer, feat):
        """선택 피처를 지도에서 잠시 강조."""
        canvas = self.iface.mapCanvas()
        geom = feat.geometry()
        dest = canvas.mapSettings().destinationCrs()
        g_map = self._locate_geometry_for_canvas(layer, geom) if geom and not geom.isNull() else None
        try:
            if QgsFeatureIds is not None and hasattr(canvas, "flashFeatureIds"):
                fids = QgsFeatureIds()
                fids.add(feat.id())
                canvas.flashFeatureIds(layer, fids)
                return
        except Exception:
            pass
        try:
            if g_map is not None and not g_map.isNull() and hasattr(canvas, "flashGeometries"):
                canvas.flashGeometries([g_map], dest)
        except Exception:
            pass

