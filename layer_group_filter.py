    def onFilterAttributeChanged(self, index):
        """필터 속성이 변경될 때 호출되는 메서드"""
        QgsMessageLog.logMessage(f"onFilterAttributeChanged called with index: {index}", level=Qgis_Info)
        if index >= 0:  # 유효한 인덱스인 경우에만 처리
            try:
                current_field = self.dockwidget.filterAttributeComboBox.currentText()
                QgsMessageLog.logMessage(f"Selected field: {current_field}", level=Qgis_Info)
                layer = self._current_reference_vector_layer()
                if layer:
                    self._populate_value_label_column_combo(
                        layer, self._current_value_label_field_name()
                    )
                # 필터 값 콤보박스 초기화
                self.dockwidget.filterValueComboBox.clear()
                self.dockwidget.filterValueComboBox.addItem("Loading...")
                
                # 필터 값 업데이트
                self.updateDistinctValues()
            except Exception as e:
                QgsMessageLog.logMessage(f"Error in onFilterAttributeChanged: {str(e)}", level=Qgis_Critical)

    def getLayerGroup(self, index):
        """레이어 그룹을 가져오는 메서드"""
        if index < 0 or index >= len(self.allGroups):
            return None
        entry = self.allGroups[index]
        if len(entry) >= 3 and entry[2] is not None:
            return entry[2]
        return self.root.findGroup(entry[0])
            
    def updateGUI(self, index):
        """필터 값이 변경될 때 호출되는 메서드"""
        if index >= 0:  # 유효한 인덱스인 경우에만 처리
            try:
                current_field = self.dockwidget.filterAttributeComboBox.currentText()
                current_value = self._current_filter_value_code()
                
                if current_field and current_value and current_value != "Loading...":
                    # 필터 문자열 생성
                    filter_text = f'"{current_field}" = \'{current_value}\''
                    QgsMessageLog.logMessage(f"Generated filter text: {filter_text}", tag="Smart Layer Filter")
                    
                    layer_ids = []
                    for vl, lid in self._iter_filter_target_vector_layers():
                        vl.setSubsetString(filter_text)
                        layer_ids.append(lid)
                    if layer_ids:
                        self.iface.mapCanvas().refresh()
                        for lid in layer_ids:
                            self.iface.layerTreeView().refreshLayerSymbology(lid)
            except Exception as e:
                QgsMessageLog.logMessage(f"Error in updateGUI: {str(e)}", tag="Smart Layer Filter")

    def loadXml(self):
        fileName, _ = QFileDialog.getOpenFileName(
            self.dockwidget, "Select XML filter Preset XML ", "", "Filter preset (*.xml)"
        )
        if not fileName:
            return
        with open(fileName, "r", encoding="utf-8", errors="replace") as xml_file:
            presetXml = xml_file.read().strip()
        QgsExpressionContextUtils.setProjectVariable(QgsProject.instance(), "groupFilterPresets", presetXml)
        self.reloadProject()