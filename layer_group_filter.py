    def _locate_expression(self, field_name, search_text):
        qf = _expression_quote_field(field_name)
        qv = _expression_quote_string_literal(search_text)
        return QgsExpression(f"strpos(lower(to_string({qf})), lower({qv})) > 0")

    def _locate_field_display_label(self, layer, field_index):
        f = layer.fields().at(field_index)
        if hasattr(f, "displayName"):
            dn = f.displayName()
            if dn:
                return dn
        al = f.alias()
        if al:
            return al
        return f.name()

    def _locate_format_result_row(self, layer, feat, field_index):
        label = self._locate_field_display_label(layer, field_index)
        try:
            raw = feat.attribute(field_index)
            s = "" if raw is None else str(raw)
        except Exception:
            s = ""
        if not s:
            s = self.tr("(값 없음)")
        if len(s) > 100:
            s = s[:97] + "..."
        return self.tr("{0}: {1}").format(label, s)

    def _locate_result_tooltip(self, layer, feat, search_field_index):
        lines = []
        n = layer.fields().size()
        for i in range(n):
            if i == search_field_index:
                continue
            fld = layer.fields().at(i)
            lab = fld.displayName() if hasattr(fld, "displayName") else fld.name()
            try:
                v = feat.attribute(i)
            except Exception:
                continue
            if v is None:
                continue
            sv = str(v).strip()
            if not sv:
                continue
            if len(sv) > 70:
                sv = sv[:67] + "..."
            lines.append(f"{lab}: {sv}")
            if len(lines) >= 10:
                break
        return "\n".join(lines)

