    def layerGroupChanged(self, index):
        """레이어 그룹이 변경될 때 호출되는 메서드"""
        QgsMessageLog.logMessage(f"layerGroupChanged called with index: {index}", tag="Smart Layer Filter")
        prev = self._active_layer_group_index
        if prev is not None and prev >= 0 and prev != index:
            self._save_group_ui_state(prev)

        self._active_layer_group_index = index if index >= 0 else None

        if index < 0:
            return
        try:
            group = self.getLayerGroup(index)
            if not group:
                return

            state = self.group_ui_state.get(self._group_state_key(index))

            self.dockwidget.applyGroupFilterCheckBox.blockSignals(True)
            self.dockwidget.applyGroupFilterCheckBox.setChecked(
                state.get('apply_group_filter', True) if state else True
            )
            self.dockwidget.applyGroupFilterCheckBox.blockSignals(False)

            preferred_id = state.get('sub_layer_id') if state else None
            layer = self._populate_layer_in_group_combo(group, preferred_id)
            if not layer:
                QgsMessageLog.logMessage(
                    "그룹에 벡터 레이어가 없어 필드 목록을 채울 수 없습니다.", tag="Smart Layer Filter"
                )
                self.dockwidget.filterAttributeComboBox.clear()
                self.dockwidget.valueLabelColumnComboBox.blockSignals(True)
                self.dockwidget.valueLabelColumnComboBox.clear()
                self.dockwidget.valueLabelColumnComboBox.blockSignals(False)
                self.dockwidget.filterValueComboBox.clear()
                return

            QgsMessageLog.logMessage(f"Reference layer: {layer.name()}", tag="Smart Layer Filter")

            fields = _field_names_for_filter_ui(layer)
            QgsMessageLog.logMessage(f"Available fields: {fields}", tag="Smart Layer Filter")

            self.dockwidget.filterAttributeComboBox.blockSignals(True)
            self.dockwidget.filterOperatorComboBox.blockSignals(True)

            self.dockwidget.filterAttributeComboBox.clear()
            self.dockwidget.filterAttributeComboBox.addItems(fields)

            if state:
                self._set_filter_operator_by_symbol(state.get('operator', '='))
                att = state.get('attribute', '')
                ai = self.dockwidget.filterAttributeComboBox.findText(att)
                if ai >= 0:
                    self.dockwidget.filterAttributeComboBox.setCurrentIndex(ai)
                elif fields:
                    self.dockwidget.filterAttributeComboBox.setCurrentIndex(0)
            elif fields:
                self.dockwidget.filterAttributeComboBox.setCurrentIndex(0)
                self._set_filter_operator_by_symbol('=')

            self._populate_value_label_column_combo(
                layer, state.get("value_label", "") if state else ""
            )

            self.dockwidget.filterOperatorComboBox.blockSignals(False)
            self.dockwidget.filterAttributeComboBox.blockSignals(False)

            saved_value = state.get('value', '') if state else ''

            if not self.dockwidget.filterAttributeComboBox.count():
                self.dockwidget.filterValueComboBox.clear()
                return

            if saved_value and saved_value != 'Loading...':
                self.updateDistinctValues()
                self._select_filter_value_by_code(saved_value)
            else:
                self.onFilterAttributeChanged(self.dockwidget.filterAttributeComboBox.currentIndex())
        except Exception as e:
            QgsMessageLog.logMessage(f"Error in layerGroupChanged: {str(e)}", tag="Smart Layer Filter")

